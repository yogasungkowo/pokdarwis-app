                        <h2>Apa Itu Malaria?</h2>
                        <p>Malaria adalah penyakit infeksi menular yang disebabkan oleh parasit protozoa dari genus Plasmodium. Penyakit ini ditularkan melalui gigitan nyamuk Anopheles betina yang terinfeksi. Di daerah tropis seperti Indonesia, terutama di kawasan pesisir dan pedesaan, malaria masih menjadi salah satu ancaman kesehatan yang perlu diwaspadai.</p>
                        
                        <h3 class="mt-4">Gejala Umum Malaria</h3>
                        <p>Gejala malaria seringkali mirip dengan flu biasa, namun bisa berkembang menjadi parah jika tidak ditangani. Waspadai gejala-gejala berikut:</p>
                        <ul>
                            <li><i class="bi bi-check-circle"></i> Demam tinggi yang disertai menggigil dan keringat berlebih.</li>
                            <li><i class="bi bi-check-circle"></i> Sakit kepala parah.</li>
                            <li><i class="bi bi-check-circle"></i> Nyeri otot dan sendi di seluruh tubuh.</li>
                            <li><i class="bi bi-check-circle"></i> Mual, muntah, dan terkadang diare.</li>
                            <li><i class="bi bi-check-circle"></i> Tubuh terasa sangat lelah dan lemas.</li>
                        </ul>

                        <h3 class="mt-4">Pencegahan adalah Kunci: 3M Plus</h3>
                        <p>Cara terbaik untuk melawan malaria adalah dengan mencegah gigitan nyamuk dan memberantas sarangnya. Terapkan metode 3M Plus secara rutin:</p>
                        <ol>
                            <li><strong>Menguras:</strong> Kuras tempat penampungan air seperti bak mandi, tempayan, dan ember secara rutin agar jentik nyamuk tidak berkembang.</li>
                            <li><strong>Menutup:</strong> Tutup rapat tempat-tempat penampungan air.</li>
                            <li><strong>Mendaur Ulang:</strong> Manfaatkan kembali atau daur ulang barang bekas yang berpotensi menjadi tempat perkembangbiakan nyamuk.</li>
                        </ol>
                        <p><strong>Plus-nya adalah:</strong></p>
                        <ul>
                            <li><i class="bi bi-shield-check"></i> Menggunakan kelambu saat tidur.</li>
                            <li><i class="bi bi-shield-check"></i> Memakai obat anti-nyamuk (lotion).</li>
                            <li><i class="bi bi-shield-check"></i> Menghindari kebiasaan menggantung pakaian di dalam rumah.</li>
                            <li><i class="bi bi-shield-check"></i> Memelihara ikan pemakan jentik di kolam air.</li>
                        </ul>

